package de.hu_berlin.ensureII.sre.grammar;

public abstract class Symbol {

/*****************************************************************************
** Constructors
*****************************************************************************/
    
	public Symbol(String name){
		this.name = name;
	}
	
	public Symbol(Symbol toCopy) {
	    this.name = toCopy.name();
	}

/*****************************************************************************
** Abstract
*****************************************************************************/
	
	/**
	 * 
	 * @return
	 *     copy of this symbol.
	 */
	public abstract Symbol copy();

/*****************************************************************************
** Attributes
*****************************************************************************/
	
	protected String name;

/*****************************************************************************
** Setter and Getter
*****************************************************************************/
	
	/**
	 * 
	 * @return
	 *     name of the symbol.
	 */
	public String name(){
		return name;
	}
}
